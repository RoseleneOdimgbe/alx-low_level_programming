#include <stdio.h>

/**
 * add - this adds two integers
 * @a: this is the first integer
 * @b: this is the second integer
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - this subtracts two integers
 * @a: this is the first integer
 * @b: this is the second integer
 * Return: subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - this multiplies two integers
 * @a: this is the first integer
 * @b: this is the second integer
 * Return: multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - this divides two integers
 * @a: this is the first integer
 * @b: this is the second integer
 * Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - this is the modulus of two integers
 * @a: this is the first integer
 * @b: this is the second integer
 * Return: the final answer
 */

int mod(int a, int b)
{
	return (a % b);
}
